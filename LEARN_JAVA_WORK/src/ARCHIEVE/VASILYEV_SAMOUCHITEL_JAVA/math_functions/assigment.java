package VASILYEV_SAMOUCHITEL_JAVA.math_functions;

public class assigment {
    public static void main(String[] args) {
        int num1, num2;

        num1 = 4;
        num2 = 8;
        num2 += num1; // num2 = num2 + num1;
        // num2 is 12 and num1 is 4

        num1 = 4;
        num2 = 8;
        num2 -= num1; // num2 = num2 - num1;
        // num2 is 4 and num1 is 4

        //Similarly, Java supports multiplication and assignment (*=), division and assignment (/=),
        //and remainder and assignment (%=).

        num1 = 4;
        num2 = 8;
        num2 *= num1; // num2 = num2 * num1;
        // num2 is 32 and num1 is 4

        num1 = 4;
        num2 = 8;
        num2 /= num1; // num2 = num2 / num1;
        // num2 is 2 and num1 is 4

        num1 = 4;
        num2 = 9;
        num2 %= num1; // num2 = num2 % num1;
        // num2 is 1 and num1 is 4

    }
}


